# OPERATORS --------
# 1. ARTHEMETIC OPERATORS : + , - , * , / , % 
print("\n ARTHEMETIC OPERATORS ------------------- \n ")
print("the value of 3 + 4 = ", 3 + 4)
print("the value of 3 - 4 = ", 3 - 4)
print("the value of 3 * 4 = ", 3 * 4)
print("the value of 3 / 4 = ", 3 / 4)
print("the value of 3 % 4 = ", 3 % 4)


# 2. ASSIGNMENT OPERATORS : = , += , -= , *= , /=
print("\n ASSIGNMENT OPERATORS ------------------- \n")

a = 10
print(a)

a += 10
print(a)

a-= 10
print(a)

a *= 10
print(a)

a /= 10
print(a)

# 3. COMPARSON OPERATORS : > , < , >= , == , <= , != 
print("\n COMPARISON OPERATORS ------------------- \n")
print("a > 10" , a > 10)
print("a < 10" , a < 10)
print("a <= 10" , a <= 10)
print("a >= 10" , a >= 10)
print("a != 10" , a != 10)

# 4.  LOGICAL OPERATORS : and , or , not
print("\n LOGICAL OPERATORS ------------------- \n")
# in coding 1 can represent true and 0 can represent false
print("1 or 0 = ", 1 or 0)
print("1 and 0 = ", 1 and 0)
print("0 and 0 = ", 0 and 0)
print("true and false = ", True or False)
print("true or false = ", True and False)



